#include <iostream>
using namespace std;

int main() {
    // and operation
    int num1 = 5;
    int num2 = 3;
    int result = num1 && num2;

    cout << "Result of AND operation: " << result << endl;

    // or operation
    num1 = 5;
    num2 = 3;
    result = num1 | num2;
    cout << "Result of OR operation: " << result << endl;

    return 0;
}
